"""
Phase 4 — Retrieval Quality Evaluation (Track 2)
=================================================

Processes pipeline_report.json (generated by evaluate_pipeline.py) to compute
retrieval quality metrics offline, without calling any service.

Metrics:
  - Recall@k (k=3, 5, 10): fraction of anchor pages recovered in top-k chunks
  - Precision@k (k=3, 5): fraction of top-k chunks that are anchor pages
  - MRR: mean reciprocal rank of first anchor hit
  - anchor_stage_coverage: fraction of retrieved chunks from anchor-guided (A)
    vs semantic (B) retrieval stages

The script requires:
  - evaluation/results/pipeline_report.json   (from evaluate_pipeline)
  - evaluation/gold_standard/nota_*_expected_outputs.json (for anchor reference)

Usage:
    python -m evaluation.scripts.evaluate_retrieval
    python -m evaluation.scripts.evaluate_retrieval --json-report evaluation/results/retrieval_report.json
    python -m evaluation.scripts.evaluate_retrieval --nota 97 01
"""
from __future__ import annotations

import argparse
import json
import sys
from datetime import datetime, timezone
from pathlib import Path

_ROOT = Path(__file__).parent.parent.parent
sys.path.insert(0, str(_ROOT))

_GOLD_DIR = _ROOT / "evaluation" / "gold_standard"
_RESULTS_DIR = _ROOT / "evaluation" / "results"
_PIPELINE_REPORT = _RESULTS_DIR / "pipeline_report.json"


# ── Anchor extraction ─────────────────────────────────────────────────────────

def load_expected_outputs(nota_id: str) -> list[dict]:
    """Load the expected_outputs JSON for a nota and return the outputs list."""
    path = _GOLD_DIR / f"nota_{nota_id}_expected_outputs.json"
    if not path.exists():
        return []
    with open(path, encoding="utf-8") as f:
        data = json.load(f)
    return data.get("outputs", [])


def load_anchor_set(case_id: str, outputs: list[dict]) -> set[tuple[str, int]]:
    """
    Return {(pdf_file, page)} from blocking_rules + passed_rules anchors
    for the given case_id.
    """
    case_output = next((o for o in outputs if o.get("case_id") == case_id), None)
    if case_output is None:
        return set()

    result = case_output.get("actual_result", {})
    rag_payload = result.get("rag_payload", {})
    anchors: set[tuple[str, int]] = set()

    for br in rag_payload.get("blocking_rules", []):
        anchor = br.get("anchor", {})
        pdf = anchor.get("pdf_file", "")
        page = anchor.get("page", 0)
        if pdf:
            anchors.add((pdf, page))

    for pr in rag_payload.get("passed_rules", []):
        anchor = pr.get("anchor", {})
        pdf = anchor.get("pdf_file", "")
        page = anchor.get("page", 0)
        if pdf:
            anchors.add((pdf, page))

    return anchors


# ── Per-case metrics ──────────────────────────────────────────────────────────

def recall_at_k(chunks: list[dict], anchors: set[tuple[str, int]], k: int) -> float | None:
    """
    Fraction of anchor pages found in the top-k chunks.
    Returns None only when a case has no anchor pages at all (no blocking_rules
    and no passed_rules anchors). ROUTED cases that carry passed_rules anchors
    are scored normally. Cases with no anchors are excluded from aggregate
    retrieval metrics rather than scored as vacuously perfect, otherwise they
    bias the average upward.
    """
    if not anchors:
        return None
    top_k = {(c["pdf_file"], c["page"]) for c in chunks[:k]}
    return len(top_k & anchors) / len(anchors)


def precision_at_k(chunks: list[dict], anchors: set[tuple[str, int]], k: int) -> float | None:
    if not anchors:
        return None
    if k == 0:
        return 0.0
    top_k = [(c["pdf_file"], c["page"]) for c in chunks[:k]]
    return sum(1 for p in top_k if p in anchors) / k


def mrr(chunks: list[dict], anchors: set[tuple[str, int]]) -> float | None:
    """
    Mean reciprocal rank of first anchor hit. Returns 0.0 if never found,
    or None only when a case has no anchor pages at all (no blocking_rules and
    no passed_rules anchors). ROUTED cases that carry passed_rules anchors are
    scored normally. Cases with no anchors are excluded from the aggregate.
    """
    if not anchors:
        return None
    for rank, c in enumerate(chunks, start=1):
        if (c["pdf_file"], c["page"]) in anchors:
            return 1.0 / rank
    return 0.0


def stage_fractions(chunks: list[dict]) -> tuple[float, float]:
    """
    Returns (anchor_guided_fraction, semantic_fraction) of retrieved chunks
    based on the retrieval_stage field ('anchor_guided' or 'semantic').
    """
    if not chunks:
        return 0.0, 0.0
    n = len(chunks)
    stage_a = sum(1 for c in chunks if c.get("retrieval_stage", "") == "anchor_guided")
    stage_b = sum(1 for c in chunks if c.get("retrieval_stage", "") == "semantic")
    return stage_a / n, stage_b / n


# ── Core evaluation ───────────────────────────────────────────────────────────

def evaluate_case_retrieval(case_result: dict, outputs: list[dict]) -> dict:
    """
    Compute retrieval metrics for a single case from pipeline_report case entry.
    """
    case_id = case_result["case_id"]
    chunks = case_result.get("retrieved_chunks_metadata", [])
    anchors = load_anchor_set(case_id, outputs)

    r3  = recall_at_k(chunks, anchors, 3)
    r5  = recall_at_k(chunks, anchors, 5)
    r10 = recall_at_k(chunks, anchors, 10)
    p3  = precision_at_k(chunks, anchors, 3)
    p5  = precision_at_k(chunks, anchors, 5)
    mrr_val = mrr(chunks, anchors)
    frac_a, frac_b = stage_fractions(chunks)

    return {
        "case_id": case_id,
        "description": case_result.get("description", ""),
        "category": case_result.get("category", ""),
        "num_chunks": len(chunks),
        "num_anchors": len(anchors),
        "recall_at_3": round(r3, 4) if r3 is not None else None,
        "recall_at_5": round(r5, 4) if r5 is not None else None,
        "recall_at_10": round(r10, 4) if r10 is not None else None,
        "precision_at_3": round(p3, 4) if p3 is not None else None,
        "precision_at_5": round(p5, 4) if p5 is not None else None,
        "mrr": round(mrr_val, 4) if mrr_val is not None else None,
        "stage_a_fraction": round(frac_a, 4),
        "stage_b_fraction": round(frac_b, 4),
    }


def aggregate_retrieval_metrics(case_results: list[dict]) -> dict:
    if not case_results:
        return {}
    n = len(case_results)

    def _mean(key: str) -> tuple[float, int]:
        """Mean over cases where the metric is defined (i.e. value is not None).

        Returns None only when a case has no anchor pages at all (no
        blocking_rules and no passed_rules anchors). ROUTED cases that carry
        passed_rules anchors are scored normally and are included here.
        Cases with no anchors at all are excluded from the aggregate to
        avoid vacuous 1.0 inflation. The second tuple element is the number
        of cases that contributed to the mean.
        """
        vals = [r[key] for r in case_results if r.get(key) is not None]
        if not vals:
            return (0.0, 0)
        return (round(sum(vals) / len(vals), 4), len(vals))

    # Stage coverage across all chunks (not per-case mean)
    all_chunks_a = sum(r["num_chunks"] * r["stage_a_fraction"] for r in case_results)
    all_chunks_b = sum(r["num_chunks"] * r["stage_b_fraction"] for r in case_results)
    total_chunks = sum(r["num_chunks"] for r in case_results)

    r3, n3 = _mean("recall_at_3")
    r5, n5 = _mean("recall_at_5")
    r10, n10 = _mean("recall_at_10")
    p3, np3 = _mean("precision_at_3")
    p5, np5 = _mean("precision_at_5")
    mrr_val, n_mrr = _mean("mrr")

    return {
        "total_cases": n,
        "cases_with_anchors": n10,
        "cases_routed_excluded": n - n10,
        "recall_at_k": {"3": r3, "5": r5, "10": r10},
        "precision_at_k": {"3": p3, "5": p5},
        "mrr": mrr_val,
        "anchor_stage_coverage": {
            "anchor_guided_fraction": round(all_chunks_a / total_chunks, 4) if total_chunks else 0.0,
            "semantic_fraction":      round(all_chunks_b / total_chunks, 4) if total_chunks else 0.0,
        },
    }


# ── Main ──────────────────────────────────────────────────────────────────────

def run_retrieval_evaluation(
    pipeline_report_path: Path,
    nota_ids: list[str],
) -> dict:
    if not pipeline_report_path.exists():
        print(
            f"ERROR: pipeline_report.json not found at {pipeline_report_path}\n"
            "Run 'make eval-pipeline' (requires: make up) to generate it first.",
            file=sys.stderr,
        )
        sys.exit(2)

    with open(pipeline_report_path, encoding="utf-8") as f:
        pipeline_report = json.load(f)

    # Load expected_outputs for anchor reference (keyed by nota_id)
    outputs_by_nota: dict[str, list[dict]] = {}
    for nid in nota_ids:
        outputs_by_nota[nid] = load_expected_outputs(nid)

    # Build a quick lookup: case_id → nota_id (from pipeline report metadata)
    # We derive nota from the case_id prefix (N97, N01, N13, N66)
    _nota_map = {"N97": "97", "N01": "01", "N13": "13", "N66": "66"}

    case_results = pipeline_report.get("case_results", [])
    retrieval_results: list[dict] = []

    print(f"Processing {len(case_results)} cases from pipeline report...\n")

    for cr in case_results:
        case_id = cr.get("case_id", "")
        nota_prefix = case_id[:3]
        nota_id = _nota_map.get(nota_prefix)
        if nota_id not in nota_ids:
            continue
        outputs = outputs_by_nota.get(nota_id, [])
        result = evaluate_case_retrieval(cr, outputs)
        retrieval_results.append(result)

        # Check for missing metadata (pipeline_report generated before A3)
        if "retrieved_chunks_metadata" not in cr:
            print(
                f"  WARNING [{case_id}]: retrieved_chunks_metadata missing. "
                "Re-run eval-pipeline after applying Task A3.",
                file=sys.stderr,
            )

    aggregate = aggregate_retrieval_metrics(retrieval_results)

    # Print summary
    print("=" * 60)
    print("RETRIEVAL EVALUATION SUMMARY")
    print("=" * 60)
    if aggregate:
        rk = aggregate["recall_at_k"]
        pk = aggregate["precision_at_k"]
        asc = aggregate["anchor_stage_coverage"]
        print(f"Cases evaluated:        {aggregate['total_cases']}")
        print(f"Cases with anchors:     {aggregate['cases_with_anchors']}")
        print(f"ROUTED (excluded):      {aggregate['cases_routed_excluded']}")
        print(f"Recall@3:               {rk['3']:.4f}")
        print(f"Recall@5:               {rk['5']:.4f}")
        print(f"Recall@10:              {rk['10']:.4f}")
        print(f"Precision@3:            {pk['3']:.4f}")
        print(f"Precision@5:            {pk['5']:.4f}")
        print(f"MRR:                    {aggregate['mrr']:.4f}")
        print(f"Stage A (anchor):       {asc['anchor_guided_fraction']:.4f}")
        print(f"Stage B (semantic):     {asc['semantic_fraction']:.4f}")
    print("=" * 60)

    return {
        "timestamp": datetime.now(timezone.utc).isoformat(),
        "pipeline_report_path": str(pipeline_report_path),
        "nota_ids_evaluated": nota_ids,
        "aggregate": aggregate,
        "case_results": retrieval_results,
    }


def main() -> int:
    parser = argparse.ArgumentParser(
        description="Compute Track 2 retrieval metrics from pipeline_report.json"
    )
    parser.add_argument(
        "--pipeline-report",
        default=str(_PIPELINE_REPORT),
        metavar="PATH",
        help=f"Path to pipeline_report.json (default: {_PIPELINE_REPORT})",
    )
    parser.add_argument(
        "--nota", nargs="+", default=["97", "01", "13", "66"],
        help="Which nota(e) to evaluate (default: all)",
    )
    parser.add_argument(
        "--json-report", metavar="PATH",
        default=str(_RESULTS_DIR / "retrieval_report.json"),
        help="Write JSON report to this path",
    )
    args = parser.parse_args()

    report = run_retrieval_evaluation(
        pipeline_report_path=Path(args.pipeline_report),
        nota_ids=args.nota,
    )

    out_path = Path(args.json_report)
    out_path.parent.mkdir(parents=True, exist_ok=True)
    with open(out_path, "w", encoding="utf-8") as f:
        json.dump(report, f, indent=2, ensure_ascii=False)
    print(f"\nRetrieval report written to: {out_path}")

    return 0


if __name__ == "__main__":
    sys.exit(main())
