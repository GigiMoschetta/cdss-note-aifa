"""
M7 — PDF-Gold Decision F1
==========================

Macro F1 of rule_engine decisions vs the **author-scripted gold** stored in
`nota_NN_cases.json::expected_rule_engine.reimbursement_decision` (independent
of any rule-engine run). Every blocking rule that fires is linked to a verbatim
PDF excerpt via `pdf_derived_anchors.json` (separate anchor-coverage indicator).

Audit fix 2026-04-30:
- PRE-fix: M7 read `actual_result.reimbursement_decision` from
  `expected_outputs_v2.json`, but that file is generated by *running* the rule
  engine on each case. So M7 effectively compared `rule_engine(input)` vs
  `rule_engine(input)` and was tautologically 1.0 by construction.
- POST-fix: gold = `expected_rule_engine.reimbursement_decision` from the
  cases JSON (decision asserted by the test author, NOT by the engine). For
  the 5 N01 ROUTED cases (no `reimbursement_decision`), the heuristic uses
  `decision_status == "ROUTED"` → gold = "ROUTED".

A high F1 here means: the implemented rules + Kleene 3VL evaluator produce
the decision the test author expected, on cases the author wrote.

Output:
    macro_f1, accuracy, per_class precision/recall/f1, support
    plus n_blocking_rules_with_anchor (over total blocking rules) — the
    "anchor coverage" indicator (separate signal, NOT decisional).
"""
from __future__ import annotations

import argparse
import json
import logging
import sys
from pathlib import Path
from collections import Counter, defaultdict

_HERE = Path(__file__).parent
_ROOT = _HERE.parent.parent
_GOLD_DIR = _ROOT / "evaluation" / "gold_standard"
_CASES_DIR = _ROOT / "evaluation" / "gold_standard"  # nota_NN_cases.json (author-scripted gold)
_PIPELINE_REPORT = _ROOT / "evaluation" / "results" / "rule_engine_report.json"
_OUTPUT = _ROOT / "evaluation" / "results" / "pdf_gold_decision_f1.json"


def _f1(precision: float, recall: float) -> float:
    return (2 * precision * recall / (precision + recall)) if (precision + recall) > 0 else 0.0


def main() -> int:
    parser = argparse.ArgumentParser()
    parser.add_argument("--gold-dir", type=Path, default=_GOLD_DIR)
    parser.add_argument("--pipeline-report", type=Path, default=_PIPELINE_REPORT)
    parser.add_argument("--output", type=Path, default=_OUTPUT)
    args = parser.parse_args()
    logging.basicConfig(level=logging.INFO, format="%(levelname)-8s %(message)s")
    log = logging.getLogger("m7")

    # ── Author-scripted gold: nota_NN_cases.json::expected_rule_engine.reimbursement_decision ──
    # This is the gold the test author asserted independently of any code execution.
    # Comparing rule engine output against this gold tells us whether the implementation
    # matches expectations — NOT whether it is self-consistent (which is tautological).
    gold: dict[str, dict] = {}
    for nota in ["01", "13", "66", "97"]:
        cpath = args.gold_dir / f"nota_{nota}_cases.json"
        if not cpath.exists():
            log.warning(f"missing {cpath}")
            continue
        cd = json.loads(cpath.read_text())
        cases = cd.get("cases", cd) if isinstance(cd, dict) else cd
        for c in cases:
            cid = c.get("id") or c.get("case_id")
            if not cid:
                continue
            er = c.get("expected_rule_engine", {}) or {}
            decision = er.get("reimbursement_decision")
            ds = er.get("decision_status", "")
            # ROUTED: reimbursement_decision is None, decision_status='ROUTED'
            if decision is None and ds == "ROUTED":
                decision = "ROUTED"
            elif decision is None:
                # No expected decision in the case file — skip rather than guess
                continue
            gold[cid] = {"expected_decision": decision}

    log.info(f"Loaded author-scripted gold for {len(gold)} cases (independent of rule-engine run)")

    # ── Anchor coverage: separate signal from decision F1 ──
    # PDF-anchor coverage is a quality indicator on the rules, not on decisions.
    n_total_blocking = 0
    n_blocking_with_anchor = 0
    anchor_status_counter: Counter = Counter()
    for nota in ["01", "13", "66", "97"]:
        path = args.gold_dir / f"nota_{nota}_expected_outputs_v2.json"
        if not path.exists():
            continue
        d = json.loads(path.read_text())
        for o in d.get("outputs", []):
            blocking = o.get("pdf_gold", {}).get("blocking_rules_with_pdf_anchor", [])
            for br in blocking:
                n_total_blocking += 1
                status = br.get("anchor_status", "NO_ANCHOR_AVAILABLE")
                anchor_status_counter[status] += 1
                if status in ("VERBATIM_FOUND", "APPROX_FOUND", "LOW_SIM_FOUND"):
                    n_blocking_with_anchor += 1

    # Compare with rule_engine_report.json
    if not args.pipeline_report.exists():
        log.error(f"Pipeline report missing: {args.pipeline_report}")
        return 1

    rep = json.loads(args.pipeline_report.read_text())
    results = rep.get("results", [])
    if not results:
        log.error("rule_engine_report has no results")
        return 1

    # Per-class TP/FP/FN
    cm: dict[str, dict[str, int]] = defaultdict(lambda: defaultdict(int))
    n_total = 0
    n_correct = 0
    missing_in_gold: list[str] = []

    for r in results:
        cid = r.get("case_id")
        # rule_engine_report.json schema: checks.reimbursement_decision.actual
        checks = r.get("checks", {})
        actual_decision = (
            checks.get("reimbursement_decision", {}).get("actual")
            if isinstance(checks.get("reimbursement_decision"), dict)
            else None
        )
        # If decision is None but route_to is set → ROUTED
        if actual_decision is None and isinstance(checks.get("decision_status"), dict):
            ds = checks["decision_status"].get("actual", "")
            if ds == "ROUTED":
                actual_decision = "ROUTED"
        actual_decision = actual_decision or r.get("actual_decision") or r.get("decision")
        gold_entry = gold.get(cid)
        if gold_entry is None:
            missing_in_gold.append(cid)
            continue
        gold_decision = gold_entry["expected_decision"]
        n_total += 1
        if actual_decision == gold_decision:
            n_correct += 1
        cm[gold_decision][actual_decision or "NONE"] += 1

    # Compute macro F1
    classes = sorted(set(cm.keys()) | {k for v in cm.values() for k in v.keys()})
    per_class: dict[str, dict] = {}
    f1_sum = 0.0
    f1_count = 0
    for cls in classes:
        tp = cm[cls].get(cls, 0)
        fn = sum(cm[cls].get(k, 0) for k in classes if k != cls)
        fp = sum(cm[k].get(cls, 0) for k in classes if k != cls)
        precision = tp / (tp + fp) if (tp + fp) > 0 else 0.0
        recall = tp / (tp + fn) if (tp + fn) > 0 else 0.0
        f1_val = _f1(precision, recall)
        per_class[cls] = {
            "tp": tp, "fp": fp, "fn": fn,
            "precision": round(precision, 4),
            "recall": round(recall, 4),
            "f1": round(f1_val, 4),
            "support": tp + fn,
        }
        if (tp + fn) > 0:
            f1_sum += f1_val
            f1_count += 1
    macro_f1 = f1_sum / f1_count if f1_count > 0 else 0.0
    accuracy = n_correct / n_total if n_total > 0 else 0.0

    out = {
        "metric": "M7_pdf_gold_decision_f1",
        "description": "Macro F1 of rule_engine decisions vs PDF-anchored expected_outputs_v2",
        "tautological": False,
        "anchor_coverage": {
            "n_blocking_total": n_total_blocking,
            "n_blocking_with_pdf_anchor": n_blocking_with_anchor,
            "rate": round(n_blocking_with_anchor / n_total_blocking, 4) if n_total_blocking else None,
            "by_status": dict(anchor_status_counter),
        },
        "aggregate": {
            "n_cases": n_total,
            "accuracy": round(accuracy, 4),
            "macro_f1": round(macro_f1, 4),
            "n_missing_in_gold": len(missing_in_gold),
        },
        "per_class": per_class,
        "confusion_matrix": {k: dict(v) for k, v in cm.items()},
        "missing_in_gold": missing_in_gold[:20],
    }
    args.output.parent.mkdir(parents=True, exist_ok=True)
    args.output.write_text(json.dumps(out, indent=2, ensure_ascii=False))
    log.info(f"M7 macro_f1={macro_f1:.4f}, anchor_coverage={n_blocking_with_anchor}/{n_total_blocking} → {args.output}")
    return 0


if __name__ == "__main__":
    sys.exit(main())
